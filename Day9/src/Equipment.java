
public class Equipment {
    protected String name;
    protected int level;
    public void setLevel(int level){
        this.level =level;
    }

    public Equipment() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getLevel(){
        return level;

    }
}
